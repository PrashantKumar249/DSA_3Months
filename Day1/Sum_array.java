public class Sum_array {
  public static void main(String [] args){
     int[] nums = {10,20,30,40,50};
     int Sum = 0;
     for(int i=0; i<nums.length; i++){
       Sum= Sum+ nums[i];
     }
    System.out.println(Sum);
  }
}


